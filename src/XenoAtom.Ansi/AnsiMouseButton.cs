namespace XenoAtom.Ansi;

/// <summary>
/// Mouse buttons used by common terminal mouse protocols.
/// </summary>
public enum AnsiMouseButton
{
    /// <summary>
    /// No button.
    /// </summary>
    None = 0,

    /// <summary>
    /// Left button.
    /// </summary>
    Left = 1,

    /// <summary>
    /// Middle button.
    /// </summary>
    Middle = 2,

    /// <summary>
    /// Right button.
    /// </summary>
    Right = 3,
}
